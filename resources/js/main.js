let wrapper = $(".wrapper");

let mostrarTabla = function(array) {
    let tr = $("<tr></tr>");
    let th = $("<th scope='row'>"+array[0]+"</th>");
    let td1 = $("<td>"+array[1]+"</td>");
    let td2 = $("<td>"+array[2]+"</td>");

    tr.append(th,td1,td2);
    return tr;
}

$(document).ready(function() {

    sFormulacion.forEach(function(element) {
        wrapper.append(mostrarTabla(element))
    });

    $("#servicesState").change(function() {
        let selected = $(this).find("option:selected").val();
        
        if(selected == 1) {
            wrapper.empty();
            sFormulacion.forEach(function(element) {
                wrapper.append(mostrarTabla(element))
            });
        }else if (selected == 2) {
            wrapper.empty();
            sElaboracion.forEach(function(element) {
                wrapper.append(mostrarTabla(element))
            });
        }
    });
});